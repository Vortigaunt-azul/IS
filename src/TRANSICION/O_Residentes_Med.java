// Verificar si ya existe un registro para el residente y el medicamento seleccionados
private boolean existeRegistro(String nombreResidente, String nombreMedicamento) {
    boolean existe = false;

    try {
        Connection con = Conexion.getConexion();
        PreparedStatement ps = con.prepareStatement("SELECT COUNT(*) FROM residentes_medicamentos rm " +
                                                     "JOIN residentes r ON rm.residente_id = r.id " +
                                                     "JOIN medicamentos m ON rm.medicamento_id = m.id " +
                                                     "WHERE r.nombre = ? AND m.nombre = ?");
        ps.setString(1, nombreResidente);
        ps.setString(2, nombreMedicamento);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            int count = rs.getInt(1);
            existe = count > 0;
        }

        rs.close();
        ps.close();
        con.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }

    return existe;
}
    
    private int obtenerIdResidente(String nombre) throws SQLException {
    int idResidente = -1; // Valor predeterminado si no se encuentra el ID
    Connection con = Conexion.getConexion();
    PreparedStatement ps = con.prepareStatement("SELECT id FROM residentes WHERE nombre = ?");
    ps.setString(1, nombre);
    ResultSet rs = ps.executeQuery();
    if (rs.next()) {
        idResidente = rs.getInt("id");
    }
    return idResidente;
}

private int obtenerIdMedicamento(String medicamento) throws SQLException {
    int idMedicamento = -1; // Valor predeterminado si no se encuentra el ID
    Connection con = Conexion.getConexion();
    PreparedStatement ps = con.prepareStatement("SELECT id FROM medicamentos WHERE nombre = ?");
    ps.setString(1, medicamento);
    ResultSet rs = ps.executeQuery();
    if (rs.next()) {
        idMedicamento = rs.getInt("id");
    }
    return idMedicamento;
}
    
